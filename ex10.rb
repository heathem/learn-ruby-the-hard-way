# Assigns strings to variables, uses \t to tab over, uses \\ to add one \
tabby_cat = "\tI'm tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."

# Assigns string to variable, uses triple quotes to add multiple lines of text
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

# Displays the variables above
puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat
